function instructions(w)
    
    keys=getSettings('keys');
    % s.keys.nback  = KbName({'n','b'});
    % %                  index  middle ring
    % s.keys.finger = KbName({'j','k','l'});
    instruct = {...
    [   ...
      keys.string{1} '->' keys.fingernames{1} '\n' ...
      keys.string{2} '->' keys.fingernames{2} '\n' ...
      keys.string{3} '->' keys.fingernames{3} '\n' ...
      '\n'...
    ],[ ...
      'When you see a RED cross, push the key for the number\n\n' ...
    ],[ ...
      'When you see a BLUE cross,\n',...
      'push the key for the position\n',...
    ],[ ...
      'ALSO, remeber the number after a BLUE cross.\n'...
      'if you see that number appears as the number after the next trial\n'...
      'push "' keys.fingernames{5} '" if same as 2 back\n'...
      'push "' keys.fingernames{4} '" if NOT the same as 2 back' ...
    ]};

    KbCheck;
    for i = 1:length(instruct)
      DrawFormattedText(w,instruct{i},'center','center',[ 0 0 0 ]);
      Screen('Flip',w);
      WaitSecs(.5);

      [secs, keyCode, deltaSecs] =KbWait;
      escclose(keyCode);
    end
end
