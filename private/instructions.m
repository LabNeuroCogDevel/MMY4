function instructions(w)
    
    keys=getSettings('keys');
    % s.keys.nback  = KbName({'n','b'});
    % %                  index  middle ring
    % s.keys.finger = KbName({'j','k','l'});
    instruct = {...
    [   ...
      keys.string{1} '->' keys.fingernames{1} '\n' ...
      keys.string{2} '->' keys.fingernames{2} '\n' ...
      keys.string{3} '->' keys.fingernames{3} '\n' ...
      '\n'...
    ],[ ...
      'Place your right index, middle, and ring fingers on buttons\n'...
      'corresponding to the numbers 1,2,and 3, respectively\n', ...
    ],[ ...
      'You will then see a row of three numbers,\n',...
      'two that are matching and one that is unmatched\n',...
    ],[ ...
      'Push the button that corresponds to the UNMATCHED number.\n'...
    ],[ ...
      'On trials preceded by a blue crosshair, hold the UNMATCHED number in memory.\n'...
    ],[ ...
      'When a row of "x x x" appeas, respond with the finger\n'...
      'corresponding to the unmatched number from the previous trial.\n' ...
    ]};

    KbCheck;
    for i = 1:length(instruct)
      DrawFormattedText(w,instruct{i},'center','center',[ 0 0 0 ]);
      Screen('Flip',w);
      WaitSecs(.5);

      [secs, keyCode, deltaSecs] =KbWait;
      escclose(keyCode);
    end
end
